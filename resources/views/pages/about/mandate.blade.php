                                    <h6 style="color: #C41E3A; font-weight: 600;">Section 4.</h6>
                                    <p>The Board of Regents of the University shall exercise policy-making functions in
                                        accordance with the general policies, plans, and programs on education as may be
                                        formulated by the National Board of Education and the Minister of Education and
                                        Culture and the general policies, plans, and programs on national and regional
                                        development as may be issued by the National Economic and Development Authority
                                        (NEDA). The Minister of Education and Culture as Chairman of the Board of
                                        Regents shall see to it that the general educational policies, plans, and
                                        programs as formulated and promulgated are adopted and observed by the
                                        University.</p>

                                    <h6 style="color: #C41E3A; font-weight: 600;">Section 5.</h6>
                                    <p>Subject to the provisions of Section 4 hereof, the Board of Regents shall have
                                        the following specific powers and duties in addition to the general powers as
                                        provided in Section Thirteen of Act Numbered Fourteen Hundred and Fifty-Nine, as
                                        amended, otherwise known as the Corporation Law:</p>

                                    <ul style="margin-left: 2rem;">
                                        <li>To enact for the government of the University such rules and regulations not
                                            contrary to law, as may be necessary to carry out the purpose and functions
                                            of the University.</li>
                                        <li>To receive and appropriate to the ends specified by law such sums as may be
                                            provided for the support of the University.</li>
                                        <li>To import duty-free essential commodities and materials for advanced
                                            educational or technological programs as an exception to existing laws,
                                            provided such commodities or materials are not available locally; and to
                                            receive in trust legacies, gifts, and donations of real and personal
                                            property of all kinds, to administer the same for the benefit of the
                                            University or for aid to any student in accordance with the directions and
                                            instructions of the donor, and in default thereof, in such manner as the
                                            Board of Regents may in its discretion determine.</li>
                                        <li>To approve the curricula and rules and discipline drawn by the
                                            Administrative and Academic Councils as hereinafter provided.</li>
                                        <li>To confer degrees upon successful candidates for graduation, to award
                                            honorary degrees upon persons in recognition of learning, public service,
                                            statesmanship or eminence in any field of specialization of the University
                                            and to authorize the President of the University to award certificates for
                                            completion on non-degree programs.</li>
                                        <li>To confirm appointments of the vice president, deans, directors, registrars,
                                            heads of departments, professors and other officials and employees of the
                                            University made by the President; to fix their compensation, hours of
                                            service, and such other duties and conditions as the Board of Regents may
                                            promulgate, in accordance with the provisions of existing laws; to remove
                                            them for cause after investigation and hearing.</li>
                                        <li>To establish chairs in the University and to provide fellowships for
                                            qualified faculty members and scholarships to deserving students.</li>
                                        <li>To fix tuition fees and such other necessary school charges as the Board of
                                            Regents may deem proper to impose. Such fees and charges, including other
                                            incomes generated by the University shall constitute a special trust fund
                                            for the exclusive use of the University, any provisions of existing laws to
                                            the contrary notwithstanding.</li>
                                        <li>To authorize the construction or repair of its buildings, machineries,
                                            equipment and other facilities and the purchase of necessary supplies,
                                            materials, and equipment, any provision of law to the contrary
                                            notwithstanding: provided, however, that the funds for the purpose shall
                                            come from the authorized appropriation of the University.</li>
                                    </ul>
