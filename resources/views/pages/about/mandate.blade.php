                        <div class="row">
                            <div class="col s12">
                                <h6 style="color: #C41E3A; font-weight: 600; margin-top: 1rem;">
                                    MANILA<br>
                                    MALACAÃ‘ANG<br>
                                    PRESIDENTIAL DECREE No. 1518
                                </h6>
                                <h6 style="color: #C41E3A; font-weight: 600;">
                                    CONVERTING THE PHILIPPINE COLLEGE OF ARTS AND TRADES INTO THE TECHNOLOGICAL
                                    UNIVERSITY OF THE PHILIPPINES, DEFINING ITS OBJECTIVES, ORGANIZATIONAL STRUCTURE AND
                                    FUNCTIONS, AND EXPANDING ITS PROGRAMS
                                </h6>

                                <div style="text-align: justify; line-height: 1.8; margin-top: 1rem;">
                                    <p><strong>WHEREAS,</strong> the Constitution of the Philippines directs the State
                                        to establish a complete, adequate and integrated system of education relevant to
                                        the goals of national development and prescribes that all educational
                                        institutions shall aim to develop scientific, technological and vocational
                                        efficiency;</p>

                                    <p><strong>WHEREAS,</strong> it has been declared a policy of the government under
                                        Presidential Decree No. 6-A, otherwise known as the Educational Development
                                        Decree of 1972, to reorient the educational system toward an accelerated
                                        national economic growth and meaningful social development;</p>

                                    <p><strong>WHEREAS,</strong> the Presidential Commission to Survey Philippine
                                        Education (PCSPE) recommended that high priority be given to the provision of
                                        technical and vocational education and retraining programs in order to meet
                                        current and projected needs for trained manpower in a developing society;</p>

                                    <p><strong>WHEREAS,</strong> to attain the development of vocational, scientific and
                                        technological efficiency necessary for national economic growth and meaningful
                                        social development, it is desirable that the education and training of the
                                        manpower potential in vocational, technical, industrial, technological and
                                        professional skills be approached through the technological program of
                                        education;</p>

                                    <p><strong>WHEREAS,</strong> the Philippine College of Arts and Trades as a
                                        government industrial education institution has been very closely associated
                                        with the Colombo Plan Staff College for Technician Education in Singapore, which
                                        cooperation has established regional linkage with ASEAN countries;</p>

                                    <p><strong>WHEREAS,</strong> to carry out the various programs with which to attain
                                        the objectives and goals, there is need to lead in applied research and
                                        extension services so that the Philippine College of Arts and Trades could
                                        provide consultation and advisory expertise on the national, regional and
                                        institutional levels to the other trade-technical, industrial and technological
                                        schools, both public and private, and industry in institutional planning and
                                        management, curricular development, supervisory and administrative staff
                                        development, equipment prototype research, designs and manufacture using
                                        available indigenous materials, and technology transfer and dissemination; and
                                    </p>

                                    <p><strong>WHEREAS,</strong> the Philippine College of Arts and Trades is the oldest
                                        industrial school in the country, and as such, has since 1901, proved a strong
                                        agent of change in manpower development for industrial and engineering
                                        technicians, and for supervisory and managerial skills, and has continued to do
                                        so in the implementation of the government policies in its accelerated programs;
                                    </p>

                                    <p><strong>NOW, THEREFORE, I, FERDINAND E. MARCOS,</strong> President of the
                                        Philippines, by virtue of the powers vested in me by the Constitution, do hereby
                                        order and decree:</p>
                                </div>
