                <div class="card">
                    <div class="card-content">
                        <span class="card-title" style="color: #C41E3A; font-weight: 600;">
                            <i class="material-icons left">article</i>
                            Key Sections of Presidential Decree No. 1518
                        </span>
                        <div class="row">
                            <div class="col s12">
                                <div style="text-align: justify; line-height: 1.8;">
                                    <h6 style="color: #C41E3A; font-weight: 600;">Section 1.</h6>
                                    <p>The present Philippine College of Arts and Trades is hereby converted into the
                                        "Technological University of the Philippines," hereinafter referred to in this
                                        Decree as the University. The present campus in the city of Manila shall be
                                        known as the University College.</p>

                                    <h6 style="color: #C41E3A; font-weight: 600;">Section 2.</h6>
                                    <p>The University shall provide higher and advanced vocational, technical,
                                        industrial, technological and professional education and training in the
                                        industries and technology, and practical arts leading to certificates, diplomas,
                                        and degrees. It shall provide progressive leadership in applied research,
                                        developmental studies in technical, industrial and technological fields and
                                        production using indigenous materials, effect technology transfer in the
                                        countryside; and assist in the development of small and medium scale industries
                                        in identified growth-centers.</p>

                                    <h6 style="color: #C41E3A; font-weight: 600;">Section 3.</h6>
                                    <p>The governing body of the University shall be the Board of Regents of the
                                        Technological University of the Philippines which shall be composed of the
                                        following:</p>
                                    <ul style="margin-left: 2rem;">
                                        <li>Minister of Education and Culture, Chairman</li>
                                        <li>President of the University, Vice-Chairman</li>
                                        <li>Representative of the National Economic and Development Authority (NEDA),
                                            Member</li>
                                        <li>Two (2) prominent citizens who have distinguished themselves in the
                                            professions or fields or specialization of the University, one representing
                                            the alumni association of the University, the other chosen from prominent
                                            citizens of Metro Manila, to be appointed by the President of the
                                            Philippines upon recommendation of the Minister of Education and Culture,
                                            Members.</li>
                                    </ul>

                                    <p>The first group of appointive regents shall serve for two (2) years and four (4)
                                        years, respectively. Thereafter, the appointees shall hold office for a term of
                                        four (4) years. In case of permanent vacancy, the appointee shall hold office
                                        for the unexpired term only.</p>

                                    <p>In the absence of the Minister of Education and Culture, the Minister shall
                                        authorize a representative, preferably the Deputy Minister concerned and at the
                                        least a director, to sit in the meetings of the board: Provided, however, that
                                        during these meetings the President of the University shall preside a
                                        Vice-Chairman of the Board.</p>
                                </div>
                            </div>
